// G1.3 第一个 Go 程序深度解剖
//
// package → import → func main → [[编译运行]]
// 四个看似简单的部分，每个背后都有设计哲学

package main

import "fmt"

// ============================================================
// 第一行：package main —— 为什么必须是 main？
// ============================================================
//
// 规则：Go 源码第一行声明它属于哪个包。
// 规则：要生成可执行程序，必须声明 package main。
// 规则：main 包必须含一个 func main() 作为入口。
//
// 对比 C++：
//   C++：int main() {} 是函数签名约定，没有"包"的概念
//   Go：  package main 告诉编译器"我要生成 exe 不是 .a 库"
//         package 非 main → 生成的是库文件（给别的程序用）
//
// Go 的包名决定输出类型：
//   package main → 生成 .exe（可执行文件）
//   package xxx  → 生成 .a（归档/库文件，别人 import 用的）

// ============================================================
// 第二行之后：import —— 告诉编译器去哪儿找代码
// ============================================================
//
// import "fmt" 的意思是"我要用 fmt 包里导出的函数"
// Go 编译器会自动找到这个包——标准库、三方库、本地包都一样
//
// 对比 C++：
//   C++：#include <iostream>
//       预处理器把整个头文件复制进来，编译慢的根源之一
//   Go： import "fmt"
//       编译器直接读取编译好的 fmt 包元数据（.a 文件）
//       不复制代码，只检查类型——编译飞快
//
// 你可以一次导入多个包：
//
//	import (
//	    "fmt"
//	    "os"
//	    "time"
//	)

// ============================================================
// f := "字符串" —— Go 的分号自动插入规则
// ============================================================
//
// 这是你上次的思考题，答案在这里：
//
// 实际上 Go 的词法分析器在扫描源码时，会在以下位置自动插入分号：
//
//  1. 换行前，如果最后一个标记是：
//     标识符（int, x, fmt, Println…）
//     字面量（数字、字符串、bool…）
//     break/continue/return/fallthrough/++
//     ) 或 }
//
//  2. for/if/switch/interface 等关键字不触发插入
//
// 所以你的代码：
//
//	fmt.Println("Hello") ← 换行前最后一个 token 是 )，自动插入 ;
//	                      ← 换行前最后一个 token 是 }，自动插入 ;
//
// 而这个代码会编译错误：
//
//	func add(a int, b int) int  ← return 后自动插 ;
//	return a + b                ← 单独的 a+b 没有 return，报错！
//
// 这个设计的哲学："让编译器多干活，让程序员少犯错"
// 没人会因为忘记写分号而编译失败。
//
// 行中断规则——return 不能跨行的原因：
//
//	return    ← Go 看到 return 自动补 ;，后面的 a + b 变成了"孤立代码"
//	a + b     ← 编译错误："unreachable code"
//
// 正确写法：
//
//	return a + b

// ============================================================
// func main —— 程序的真正入口
// ============================================================
//
// func main() 是整个程序的起点。
// 当操作系统运行你的 exe 时，Go 运行时先做一些初始化工作，
// 然后调用 main 包里的 main() 函数。
//
// 对比 C++：
//   C++：int main(int argc, char *argv[])
//        返回 0 表示成功，非 0 表示错误
//   Go： func main()
//        没有 argc/argv（在 os.Args 里拿）
//        不需要 return 0——默认就是成功退出
//
// 注意：
//   func main 只在 package main 中生效，且：
//   - 不能有参数  ❌ func main(args []string)
//   - 不能有返回值 ❌ func main() int
//   - 同一个包内只能有一个 main()

// ============================================================
// init 函数——Go 的特殊初始化函数
// ============================================================
//
// 除了 main，每个包还可以有 init() 函数。
// init 在 main 之前自动执行，不需要你调用。
// 多个 init 按顺序执行（先 import 的包先 init）。
//
// C++ 没有对应概念——最接近的是全局变量构造函数，但容易出问题
// Go 的 init 是确定性的，可预测的

func init() {
	fmt.Println("[init 1] 这是第一个 init 函数 - 在 main 之前执行")
}

// 每个文件可以有多个 init
func init() {
	fmt.Println("[init 2] 这是第二个 init 函数")
}

// ============================================================
// main 函数——程序入口
// ============================================================
func main() {
	fmt.Println("╔══════════════════════════════════════════╗")
	fmt.Println("║   G1.3 第一个 Go 程序深度解剖             ║")
	fmt.Println("╚══════════════════════════════════════════╝")

	// 1. 演示 import 的效果
	fmt.Println("\n=== 1. import 机制演示 ===")
	fmt.Println("fmt 包来自 Go 标准库，不需要任何第三方依赖")

	// 2. 演示分号自动插入
	fmt.Println("\n=== 2. 分号自动插入规则 ===")
	fmt.Println("下面这行在换行前自动插入了分号")
	fmt.Println("不信？试试在上一行的最后加两个中文字 → 编译照样通过")

	// 3. 演示 os.Args（对应 C++ 的 argc/argv）
	fmt.Println("\n=== 3. 命令行参数（os.Args）===")
	fmt.Printf("程序名称：%v\n", "os.Args[0]（你自己运行看看位置）")
	fmt.Println("试着：go run main.go hello world")
	fmt.Println("os.Args 会包含：[main.go hello world]")

	// 4. 编译流程演示
	fmt.Println("\n=== 4. 编译流程 ===")
	fmt.Println("Go 编译一个程序的完整流程：")
	fmt.Println("  1. go run / go build → 触发编译")
	fmt.Println("  2. 词法分析：源码 → Token")
	fmt.Println("     （扫描并自动插入分号）")
	fmt.Println("  3. 语法分析：Token → AST（抽象语法树）")
	fmt.Println("  4. 类型检查：AST 里每个类型都合法")
	fmt.Println("  5. 中间代码生成：AST → IR（SSA）")
	fmt.Println("  6. 机器码生成：IR → .exe")
	fmt.Println("  ——整个过程不到 1 秒——")

	// 5. 演示 Go 的调试辅助
	fmt.Println("\n=== 5. 对比 C++ 编译 ===")
	fmt.Println("Go 的优势：")
	fmt.Println("  - 不用头文件，import 直接引用编译好的包")
	fmt.Println("  - package+go.mod 代替 Makefile+CMakeLists")
	fmt.Println("  - go build 一条命令搞定编译+链接")
	fmt.Println("  - 编译秒级完成，不可能等 45 分钟")
}

// ============================================================
// 总结：一个 go 程序的生命周期
// ============================================================
//
//  1. 词法分析 —— 扫描 .go 源码，Token 化（同时自动插分号）
//  2. 语法分析 —— 构建 AST（检查语法是否正确）
//  3. 类型检查 —— 每个类型都合法，没有隐式转换问题
//  4. import 解析 —— 递归解析所有 import 的包
//  5. 执行初始化 —— 按依赖顺序执行各包 init()
//  6. 执行 main —— 程序正式开始
//
//     ┌─────────────┐
//     │  源码 .go    │
//     │ package main │
//     │ import "fmt" │
//     │ func main()  │  → 词法分析 → 语法分析 → 类型检查 → 生成 exe
//     └─────────────┘
//                           ↓ 运行时
//                     init() → init() → main()
